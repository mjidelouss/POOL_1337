#include <unistd.h>
int	ft_sqrt(int nb)
{
	long long int	i;
	long long int	n;

	i = 0;
	n = nb;
	if (n >= 0)
	{
		while (i * i < n)
		{
			i++;
			if (i * i == n)
			{
				return (i);
			}
		}
	}
	return (0);
}
